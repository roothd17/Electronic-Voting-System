package com.wipro.bean;

public class EOBean 
{
	private String name;
	private String key;
	
	public EOBean()
	{}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
}
